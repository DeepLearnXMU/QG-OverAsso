class QueryProducer(Resource):
    def post(self):
        print("getting a post request ....")
        dialog_turns = None
        post_data = request.form.to_dict(flat=False)
        if "dialog_turns" in post_data:
            dialog_turns = post_data["dialog_turns"]

        if dialog_turns is None:
            return {}

        dialogue = ' '.join(dialog_turns)
        input_dict = {'dialogue': [dialogue], 'query': ['None']}
        query = parse(input_dict)[0]
        logger.info(query)
        return query

