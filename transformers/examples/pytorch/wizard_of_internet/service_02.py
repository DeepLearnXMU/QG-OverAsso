def parse(input_dict: dict):
    final_predictions = []
    # input_dict: {'dialogue': [dialogue_1, dialogue_2, ...]}
    batch_size = 16
    num_beams = 4
    # split to batch
    all_data = tokenizer(input_dict['dialogue'], truncation=True, return_tensors='pt')
    for i in range(0, len(input_dict['dialogue']), batch_size):
        batch = {'input_ids': all_data['input_ids'][i:i+batch_size],
                 'attention_mask': all_data['attention_mask'][i:i+batch_size]}
        # return multiple predictions (num_beams) instead of only one prediction of beam search
        predictions = model.generate(**batch, max_length=64, num_beams=num_beams, num_return_sequences=num_beams)
        predictions = tokenizer.batch_decode(predictions, skip_special_tokens=True, clean_up_tokenization_spaces=True)
        predictions = [pred.strip() for pred in predictions]

        def select_top_one(preds):
            for pred in preds:
                if data_args.remove_none:
                    if pred not in ['不检索', 'none']:
                        return pred
                else:
                    return pred

        # filter none value if set data_args.remove_none; or just return the first prediction
        predictions = [select_top_one(predictions[i:i + num_beams]) for i in
                       range(0, len(predictions), num_beams)]
        final_predictions += predictions

    # a list of prediction for each dialogue
    return final_predictions

